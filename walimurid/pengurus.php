    <p><center><h6>SMP CAWANG BARU didirikan pada tahun 2010 beralamat di Jl. Cawang Baru No.543, RT.7/RW.10, Cipinang Cempedak, Kecamatan Jatinegara, Kota Jakarta Timur, Daerah Khusus Ibukota Jakarta 13340</center></h6>
