<p>Visi</p>
<br>
<p>“TURUT MENYIAPKAN MUTU SUMBER DAYA MANUSIA YANG CERDAS, KREATIF, TERAMPIL DAN HANDAL DENGAN BEKAL IPTEK YANG DILANDASI IMTAQ”</p><br>
Indikator :
<p>1.  Terwujudnya perolehan nilai akademis siswa meningkat dari tahun ke tahun </p>
<p>2.  Terwujudnya kegiatan ekstrakurikuler terlaksana secara optimal</p>
<p>3.  Terwujudnya dan terpenuhinya saran dan prasarana menuju manajemen berbasis sekolah</p>
<p>4.  Terwujudnya disiplin warga sekolah sesuai dengan standar yang telah ditetapkan</p>
<p>5.  Terwujudnya guru dan karyawan bekerja secara profesional</p>
<p>6.  Terwujudnya lingkungan sekolah bersih dan indah.</p>
<p>7.  Terwujudnya kerja sama dan kebersamaan diantara warga sekolah</p>
<p>8.  Terwujudnya warga sekolah peduli terhadap lingkungan </p>
<p>9.  Terwujudnya menjunjung tinggi  nilai-nilai budaya</p>
<br>

<p> Misi</p>
<p>1.  Menciptakan lingkungan pembelajaran yang kondusif dalam upaya meningkatkan mutu pembelajaran</p>
<p>2.  Menumbuh kembangkan semangat keunggulan dan bernalar sehat kepada peserta didik, pendidik dan tenaga kependidikan 
       sehingga berkemauan kuat untuk terus maju</p>
<p>3.  Meningkatkan komitmen seluruh pendidik dan tenaga kependidikan terhadap tugas pokok dan fungsinya</p>
<p>4.  Mengembangkan Teknologi Informasi dan Komunikasi dalam pembelajaran dan administrasi sekolah</p>
<p>5.  Mengembangkan minat, bakat dan kreativitas peserta didik agar tumbuh dan berkembang sesuai dengan potensi yang dimiliki</p>
<p>6.  Membangun kepercayaan dan kepedulian alumni terhadap almamaternya</p>
<br>

<p> Tujuan</p>
<p>1.  Menghasilkan standar kompetensi kelulusan </p>
<p>2.  Menghasilkan dokumen kurikulum berstandar nasionaldi sekolah</p>
<p>3.  Tercapainya proses belajar mengajar dengan berbadai metode pembelajaran yang sesuai dengan tuntutan kurikulum nasional</p>
<p>4.  Terciptanya mutu SDM tenaga pendidik dan tenaga kependidikan yang profesional</p>
<p>5.  Merintis pemenuhan fasilitas sekolah yang lengkap</p>
<p>6.  Pengelolaan sekolah dengan model manajemen berbasis sekolah</p>
<p>7.  Pelaksanaan penilaian sesuai standar</p>



